int	main(int argc, char **argv)
{
	char *str;
	int n;

	n = 1;
	if (argc == 2)
	{
		str = argv[1];
		while (*str)
		{
			if (*str >= 65 && *str <= 90)
				n = *str - 64;
			else if (*str >= 97 && *str <= 122)
				n = *str - 96;
			while (n)
			{
				write(1, str, 1);
				n--;
			}
			n = 1;
			str++;
		}
		write(1, "\n", 1);
	}
}
